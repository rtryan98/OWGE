void imgui_init(HWND hwnd, Render_Engine* render_engine)
{
    ImGui::CreateContext();
    ImGui_ImplWin32_Init(hwnd);
    auto ctx = render_engine->get_context();
    auto increment = ctx->device->GetDescriptorHandleIncrementSize(D3D12_DESCRIPTOR_HEAP_TYPE_CBV_SRV_UAV);
    D3D12_CPU_DESCRIPTOR_HANDLE font_srv_cpu_descriptor = ctx->cbv_srv_uav_descriptor_heap->GetCPUDescriptorHandleForHeapStart();
    font_srv_cpu_descriptor.ptr += IMGUI_DESCRIPTOR_INDEX * increment;
    D3D12_GPU_DESCRIPTOR_HANDLE font_srv_gpu_descriptor = ctx->cbv_srv_uav_descriptor_heap->GetGPUDescriptorHandleForHeapStart();
    font_srv_gpu_descriptor.ptr += IMGUI_DESCRIPTOR_INDEX * increment;
    ImGui_ImplDX12_Init(ctx->device, MAX_CONCURRENT_GPU_FRAMES, DXGI_FORMAT_R8G8B8A8_UNORM,
        ctx->cbv_srv_uav_descriptor_heap, font_srv_cpu_descriptor, font_srv_gpu_descriptor);

    ImPlot::CreateContext();

    imgui_set_theme();
}

void imgui_new_frame()
{
    ImGui_ImplWin32_NewFrame();
    ImGui_ImplDX12_NewFrame();
    ImGui::NewFrame();
}

void imgui_shutdown()
{
    ImGui_ImplDX12_Shutdown();
    ImGui_ImplWin32_Shutdown();
    ImPlot::DestroyContext();
    ImGui::DestroyContext();
}
